    public static function add($values = [])
    {
        if (!$values) {
            return [false, false];
        }
        if ($entity = self::unique_one($values['m_trans_on'])) {
            return self::edit($entity->m_id, $values);
        }

        $entity = new self();
        foreach ($values as $key => $val) {
            $entity->$key = $val;
        }
        $entity->m_added_on = date('Y-m-d H:i:s');
        $res = $entity->save();

        return [$res, $entity];
    }

    public static function unique_one($m_trans_on)
    {
        return self::findOne(['m_trans_on' => $m_trans_on]);
    }

    public static function edit($id, $values = [])
    {
        if (!$values) {
            return [false, false];
        }

        $entity = self::findOne($id);

        if (!$entity) {
            return [false, false];
        }

        foreach ($values as $key => $val) {
            if ($val) {
                $entity->$key = $val;
            }
        }
        $entity->m_update_on = date('Y-m-d H:i:s');
        $res = $entity->save();

        return [$res, $entity];
    }

    /**
     * https://blog.csdn.net/USTBHacker/article/details/8365756
     * 获取当天数据接口
     *
     * @time 2019.11.18
     */
    public static function get_stock_data($stock_id, $cat = 'sz')
    {
        $base_url = "http://qt.gtimg.cn/q=%s%s";
        $ret = AppUtil::httpGet(sprintf($base_url, $cat, $stock_id));
        $ret = AppUtil::check_encode($ret);
        $ret = explode('~', $ret);

        $data = [];
        if (is_array($ret) && count($ret) > 40) {
            $dt = $ret[30];
            $trans_on = substr($dt, 0, 4)
                . '-' . substr($dt, 4, 2)
                . '-' . substr($dt, 6, 2);

            $data = [
                "turnover" => $ret[37],                             //交易量(单位：万)
                "close" => $ret[3],                                 //收盘价
                "m_trans_on" => $trans_on,                          //交易日
            ];
        }

        return $data;
    }

    /**
     * 获取当天数据
     *
     * 000001 => 上证指数
     * 399001 => 深证指数
     * 510500 => 500ETF
     *
     * @time 2019.11.18
     */
    public static function update_curr_day()
    {
        $data1 = self::get_stock_data('510500', 'sh');
        $data2 = self::get_stock_data('000001', 'sh');
        $data3 = self::get_stock_data('399001', 'sz');
        if ($data1
            && $data2
            && $data3
            && $data1['m_trans_on'] == $data2['m_trans_on']
            && $data1['m_trans_on'] == $data3['m_trans_on']) {
            self::pre_insert(
                $data1['turnover'], $data1['close'],
                $data2['turnover'], $data2['close'],
                $data3['turnover'], $data3['close'],
                $data3['m_trans_on']);
        }
    }

    /**
     * 插入数据 入口
     *
     * @time 2019-11-18 PM
     */
    public static function pre_insert(
        $stf_turnover,
        $stf_close,
        $sh_turnover,
        $sh_close,
        $sz_turnover,
        $sz_close,
        $trans_on
    ) {
        if ($trans_on != date('Y-m-d')) {
            return false;
        }

        $sum_turnover = $sh_turnover + $sz_turnover;
        $insert = [
            'm_etf_turnover' => $stf_turnover,
            'm_etf_close' => $stf_close,
            'm_sh_turnover' => $sh_turnover,
            'm_sh_close' => $sh_close,
            'm_sz_turnover' => $sz_turnover,
            'm_sz_close' => $sz_close,
            'm_sum_turnover' => $sum_turnover,
            'm_cus_rate' => $sum_turnover > 0 ? round($stf_close / $sum_turnover * 100000000, 3) : 0,
            'm_trans_on' => $trans_on,
        ];
        list($res) = self::add($insert);

        // 添加今天统计数据
        if ($res) {
            StockMainStat::cal($trans_on);
            StockMainResult::cal_one($trans_on);
        }

        return true;
    }

    /**
     * 获取数据最新更新时间
     *
     * @time 2019-11-21 AM
     */
    public static function get_latest_update_on()
    {
        $latest_one = self::find()->where([])->asArray()->orderBy('m_trans_on desc')->limit(1)->one();
        return $latest_one['m_update_on'];
    }

    /**
