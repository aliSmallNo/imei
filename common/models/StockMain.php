    {
        foreach (self::$sz_trans as $k => $sz) {
            $trans_on = date('Y-m-d', strtotime($k));
            $etf = static::$etf500_data[$k] ?? 0;
            $sh = static::$sh_trans[$k] ?? [];

            self::pre_insert(
                0,
                $etf ?? 0,
                $sh ? $sh[1] : 0,
                $sh ? $sh[0] : 0,
                $sz ?? 0,
                0,
                $trans_on);
            echo $trans_on . PHP_EOL;
        }
    }

